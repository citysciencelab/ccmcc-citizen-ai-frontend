const outputMap = {
  "CO2 Reduction Total": "output1",
  "Happiness Total": "output2",
  "Health Total": "output3",
  "Political Stability Total": "output4",
  "Resources Total": "output5",
  "output1": "CO2 Reduction Total",
  "output2": "Happiness Total",
  "output3": "Health Total",
  "output4": "Political Stability Total",
  "output5": "Resources Total"
}

const repluck = results => outputs.map(output => {
  const outputKey = outputMap[output.id]
  return {
    id: output.id,
    labels: results.map(r => r._time),
    data: results.map(r => r[outputKey] + outputOffset)
  }
})

export const mockResponse = () => {
  const ret = { results: [] }
  const rand = Math.pow(Math.random(), 2) * 4
  const rnd = {
    "CO2 Reduction Total": Math.random() * rand,
    "Happiness Total": Math.random() * rand,
    "Health Total": Math.random() * rand,
    "Political Stability Total": Math.random() * rand,
    "Resources Total": Math.random() * rand
  }
  _resp.results.forEach(_ => {
    const r = {}
    for (const key in _) {
      r[key] = _[key] * (key === '_time' ? 1 : rnd[key])
    }
    ret.results.push(r)
  })
  return repluck(ret.results)
}

